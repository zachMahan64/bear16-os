        inc a0
        clr a1
        jmp blit_strl_rom_subr_exit
    blit_strl_rom_tab:
        add a1, a1, 2
        jmp blit_strl_rom_subr_exit
    blit_strl_rom_do_update_cursor:
        mov s0, a1
        mov s1, a0
        jmp blit_strl_rom_do_update_cursor_exit

